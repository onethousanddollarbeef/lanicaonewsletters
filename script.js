const languageNames = {
  en: "English",
  zhHans: "Simplified Chinese",
  zhHant: "Traditional Chinese",
};

const buttons = document.querySelectorAll("[data-language]");
const translatedElements = document.querySelectorAll("[data-i18n]");
const subscribeForm = document.querySelector(".subscribe-form");

function setLanguage(language) {
  const dictionary = translations[language] || translations.en;

  translatedElements.forEach((element) => {
    const key = element.dataset.i18n;

    if (dictionary[key]) {
      element.textContent = dictionary[key];
    }
  });

  buttons.forEach((button) => {
    const isActive = button.dataset.language === language;
    button.classList.toggle("active", isActive);
    button.setAttribute("aria-pressed", String(isActive));
    button.setAttribute("aria-label", `Translate to ${languageNames[button.dataset.language]}`);
  });

  document.documentElement.lang = language === "en" ? "en" : language === "zhHans" ? "zh-Hans" : "zh-Hant";
  localStorage.setItem("newsletter-language", language);
}

buttons.forEach((button) => {
  button.addEventListener("click", () => {
    setLanguage(button.dataset.language);
  });
});

subscribeForm?.addEventListener("submit", (event) => {
  event.preventDefault();
});

setLanguage(localStorage.getItem("newsletter-language") || "en");
